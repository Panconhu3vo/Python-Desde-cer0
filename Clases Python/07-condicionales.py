# Condicionales

edad = 18

if edad >= 40: # Evalua si la condicion es verdadera
    print ("Eres demasiado Viejo") # Codigo que se ejecuta en el caso de que la condicon sea verdadera
    
elif edad >= 18: # Evalua una segunda condicion si la condicion es verdadera
    print("Puedes pasar") # Codigo que se ejecuta en el caso de que la condicon sea verdadera
    
else: # Si la condicion anterior es falsa
    print("No puedes pasar") # Se ejecutara todo lo de adentro