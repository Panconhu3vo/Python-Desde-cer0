'Juan' # Tipo de dato String, podemos usar "" o ''

1 # Tipo de dato Int, numeros del 0 al infinito y del negativo al infinito

0,1 # Tipo de dato float, numeros enteros y decimales

True # Tipo de dato Bool, Almacena verdadro(True) o falso(Flase), sirve para poner condiciones

